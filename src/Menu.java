    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
    int barisTerpilih = tabelbarang.getSelectedRow(); 

    // 1. Pengecekan Baris Terpilih
    if (barisTerpilih == -1) {
        JOptionPane.showMessageDialog(this, 
                "Pilih baris data yang ingin dihapus terlebih dahulu.", 
                "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    // 2. Konfirmasi Penghapusan
    int konfirmasi = JOptionPane.showConfirmDialog(this,
            "Anda yakin ingin menghapus data pada baris terpilih?", 
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
    // 3. Ambil Kode Barang (Primary Key) dari baris terpilih
    // Berdasarkan struktur baru, ini adalah kolom 'id_menu' yang berada di indeks 0
    String idMenu = tabelbarang.getModel().getValueAt(barisTerpilih, 0).toString(); 
    
    // 4. Eksekusi Perintah DELETE ke Database
    String sql = "DELETE FROM menu WHERE id_menu = ?";
    
    // Menggunakan Config.configDB() untuk mendapatkan koneksi ke db_coffeeshop
    java.sql.Connection conn = (Connection) Config.configDB();
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
    
    pst.setString(1, idMenu); 
    
    pst.executeUpdate(); // Gunakan executeUpdate() untuk operasi DELETE, INSERT, UPDATE
    
    // 5. Tampilkan Pesan Sukses
    JOptionPane.showMessageDialog(null, "Data menu dengan ID " + idMenu + " berhasil dihapus!");
    
    } catch (Exception e) {
    // Tangani Error SQL (misalnya: gagal koneksi, data tidak ditemukan)
        JOptionPane.showMessageDialog(this, 
        "Gagal menghapus data. Error: " + e.getMessage(), 
        "Error", JOptionPane.ERROR_MESSAGE);
}

    // 6. Refresh Tampilan (Pastikan baris ini di luar block 'try-catch' agar tetap dijalankan)
    load_table(); // Memuat ulang data dari database ke JTable
    kosong(); // Membersihkan field input
    }
    }//GEN-LAST:event_btnhapusActionPerformed

