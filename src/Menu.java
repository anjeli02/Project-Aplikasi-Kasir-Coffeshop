    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
                                             
        try {
            // 1. Ambil data dari field input
            // txtkodebarang digunakan sebagai primary key (id_menu) untuk WHERE clause
            String idMenu = txtkodebarang.getText();
            String namaMenu = txtnamabarang.getText();
            String stok = txtjumlahbarang.getText(); // diasumsikan kolom ini adalah 'stok'
            String harga = txthargajual.getText();   // diasumsikan kolom ini adalah 'harga'

            // Pengecekan agar field tidak kosong
            if (idMenu.isEmpty() || namaMenu.isEmpty() || stok.isEmpty() || harga.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi untuk mengedit data.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 2. Buat query UPDATE menggunakan PreparedStatement
            String sql = "UPDATE menu SET nama_menu=?, stok=?, harga=? WHERE id_menu=?";

            // 3. Dapatkan koneksi dan siapkan statement
            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            // Menetapkan nilai ke placeholder (?) sesuai urutan di query:
            pst.setString(1, namaMenu);  // SET nama_menu
            pst.setString(2, stok);      // SET stok (Jumlah Barang)
            pst.setString(3, harga);     // SET harga (Harga Jual)
            pst.setString(4, idMenu);    // WHERE id_menu (Kode Barang)

            // 4. Eksekusi query
            int rowsAffected = pst.executeUpdate();

            // 5. Tampilkan pesan
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data menu dengan ID " + idMenu + " berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal mengubah data. ID Menu tidak ditemukan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            // Tangani Error SQL 
            JOptionPane.showMessageDialog(this,
                    "Gagal mengedit data. Error: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        // 6. Refresh tampilan dan bersihkan field
        load_table();
        kosong();
    
    }//GEN-LAST:event_btneditActionPerformed

