    private void btnhapuslaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapuslaporanActionPerformed
        int baris = tabelbarang.getSelectedRow();
    if (baris != -1) {
        String id = tabelbarang.getValueAt(baris, 0).toString();
        int jawab = JOptionPane.showConfirmDialog(null, "Hapus data " + id + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (jawab == JOptionPane.YES_OPTION) {
            try {
                Connection c = Config.configDB();
                Statement s = c.createStatement();
                s.executeUpdate("DELETE FROM kasir WHERE no_factur='" + id + "'");
                s.close();
                load_table(); // Refresh tabel
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Hapus: " + e.getMessage());
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus terlebih dahulu!");
    }

    }//GEN-LAST:event_btnhapuslaporanActionPerformed

