    private void load_table() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No Faktur");
    model.addColumn("Tanggal");
    model.addColumn("ID Customer");
    model.addColumn("Total");

    try {
        Connection c = Config.configDB(); 
        Statement s = c.createStatement();
        String sql = "SELECT * FROM kasir"; //mngambil data dari tabel header kasir
        ResultSet r = s.executeQuery(sql);

        while (r.next()) {
            model.addRow(new Object[]{
                r.getString("no_factur"),
                r.getString("tanggal"),
                r.getString("id_customer"),
                r.getString("total")
            });
        }
        tabelbarang.setModel(model);
        hitungTotal(); // Hitung total setiap kali tabel di-load
    } catch (Exception e) {
        System.out.println("Error Load Table: " + e.getMessage());
    }
}

private void hitungTotal() {
    int totalPemasukan = 0;
    for (int i = 0; i < tabelbarang.getRowCount(); i++) {
        // Mengambil nilai dari kolom 'Total' (indeks 3)
        int nilai = Integer.parseInt(tabelbarang.getValueAt(i, 3).toString());
        totalPemasukan += nilai;
    }
    txt_hasilpemasukan.setText("Rp. " + String.valueOf(totalPemasukan));
}

